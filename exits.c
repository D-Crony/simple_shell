/**
 * *_strncpy - string to copy
 * @dest: copied destination string
 * @src: string source
 * @n: character number to copied
 * Return: string concatenated
 */
char *_strncpy(char *dest, char *src, int n)
{
	int a, b;
	char *s = dest;

	a = 0;
	while (src[a] != '\0' && a < n - 1)
	{
		dest[a] = src[a];
		a++;
	}
	if (a < n)
	{
		b = a;
		while (b < n)
		{
			dest[b] = '\0';
			b++;
		}
	}
	return (s);
}

/**
 * *_strncat - two strings to concatenate
 * @dest: first string
 * @src: second string
 * @n: numbers of bytes maximally used
 * Return: string concatenate
 */
char *_strncat(char *dest, char *src, int n)
{
	int a, b;
	char *s = dest;

	a = 0;
	b = 0;
	while (dest[a] != '\0')
		a++;
	while (src[b] != '\0' && b < n)
	{
		dest[a] = src[b];
		a++;
		b++;
	}
	if (b < n)
		dest[a] = '\0';
	return (s);
}

/**
 * *_strchr - string character to locate
 * @s: parsed string
 * @c: looking for number of character
 * Return: memory area pointer s
 */
char *_strchr(char *s, char c)
{
	do {
		if (*s == c)
			return (s);
	} while (*s++ != '\0');

	return (NULL);
}
